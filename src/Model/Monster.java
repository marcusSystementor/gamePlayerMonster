package Model;

public class Monster {

    private int id;
    private String type;

    public Monster() {

    }

    public Monster(String type) {
        this.type = type;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
}
